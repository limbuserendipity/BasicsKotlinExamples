/*
Task7: Повторение строк
    Задание: Напишите программу,
    которая принимает от пользователя целое число n
    и строку s, затем выводит строку s,
    повторенную ровно n раз.
    Пример: n = 6, s = "I" -> "IIIIII"
            n = 5, s = "Hello" -> "HelloHelloHelloHelloHello"
 */
fun task7(){
    println("Введите число n")
    val n = readLine()!!.toInt()
    println("Введите строку s")
    val s = readLine()
    repeat(n){
        print(s)
    }
    println()
}

/*
Task8: Символ строки
    Задание: Дана переменная val hw = "Hello world"
    необходимо посчитать сколько символов 'l' находиться в переменной hw
    Подсказка: мы можем получить символ строки используя [index],
    т.е. hw[0] == 'H'
    Подсказка 2: мы можем получить длину всей строки через .length
    т.е. hw.length == 11
    Вывод: программа должна вывести в консоль сообщение:
        "В строке $hw $count символов l"
 */

fun task8(){
    var count = 0
    val hw = "Hello world"

    repeat(hw.length){
        if(hw[it] == 'l'){
            count = count + 1
        }
    }
    println(count)
}

/*
Task9: Kakoe CloBo
    Задание: Пользователь вводит слово, узнай к какому языку принадлежит это слово,
    к русскому или английскому.
    Подсказка: используй Range от, а до я (а..я) и от (a..z) чтобы узнать
    к какому алфавиту принадлежит буква в слове,
    если все буквы в слове принадлежат к русскому алфавиту то слово русское
 */
fun task9(){
    println("Введите слово")
    val word = readLine()!!
    var isEng = true
    var isRu = true
    repeat(word.length){
        if(word[it] !in 'a'..'z'){
            isEng = false
        }
        if(word[it] !in 'а'..'я'){
            isRu = false
        }
    }
    if(isEng){
        println("слово английское")
    }
    if(isRu){
        println("слово русское")
    }
}

